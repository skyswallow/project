module ResourcesHelper

  def find_by_type(type_id, num)
    return Resource.find_by_type(type_id, num)
  end

  def find_new_resources(num)
    return Resource.find_new_resources(num)
  end

  def find_hot_resources(num)
    return Resource.find_hot_resources(num)
  end

  def find_by_parent_id(parent_id)
    return ResourceType.find_by_parent_id(parent_id)
  end
  
end